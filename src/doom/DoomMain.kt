    @SourceCode.Exact
    @G_Game.C(G_Game.G_DoCompleted)
    protected fun DoCompleted() {
        gameaction = gameaction_t.ga_nothing
        for (i in 0 until Limits.MAXPLAYERS) {
            if (playeringame[i]) {
                G_PlayerFinishLevel@ run { // take away cards and stuff
                    players[i].PlayerFinishLevel()
                }
            }
        }
